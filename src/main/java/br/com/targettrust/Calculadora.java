package br.com.targettrust;

public class Calculadora {
    private double MAX_RESULT = 100000000;

    public double somar(double a, double b) {
        return a + b;
    }

    private double multiplicar(double a, double b) {
        return a * b;
    }

    double subtrair(double a, double b) {
        return a - b;
    }
}
